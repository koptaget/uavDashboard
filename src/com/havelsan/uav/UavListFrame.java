    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String uavCodeValue = uavCode.getText();
        Float uavBatteryPercantageValue = Float.parseFloat(uavBatteryPercantage.getText());
        String uavGeoPositionValue = uavGeoPosition.getText();
        Float uavSpeedValue = Float.parseFloat(uavSpeed.getText());
        model = (DefaultTableModel) tableUavs.getModel();
        Uav uav = uavService.addUavToList(uavCodeValue, uavGeoPositionValue, uavSpeedValue, uavBatteryPercantageValue);

        model.addRow(new Object[]{uav.getUavCode(), uavUtil.calculateRemainBatteryPercantage(uav), uav.getGeoPosition(), uav.getUavSpeed(), uavUtil.calculateTotalFlightTime(uav.getUavFlightStartTime())});

    }//GEN-LAST:event_btnAddActionPerformed


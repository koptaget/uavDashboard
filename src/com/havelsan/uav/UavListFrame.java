    public UavListFrame() {
        initComponents();
        Timer timer = new Timer();
        TimerTask timerTask = new TimerTask() {
            public void run() {
                List<Uav> uavList = uavService.getUavList();
                model = (DefaultTableModel) tableUavs.getModel();
                model.getDataVector().removeAllElements();
                uavList.stream().forEach(uav -> {
                    model.addRow(new Object[]{uav.getUavCode(), uavUtil.calculateRemainBatteryPercantage(uav), uav.getGeoPosition(), uav.getUavSpeed(), uavUtil.calculateTotalFlightTime(uav.getUavFlightStartTime())});
                });
            }
        };
        timer.scheduleAtFixedRate(timerTask, 0, 1000);
        sort();
    }

